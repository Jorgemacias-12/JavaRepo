package com.jamz.Images;

/**
 * This class can resize Images
 * 
 */

public class reScale
{
    
    /**
     * Class variables.
     */
    
    private int Width;
    private int Height;
    
    /**
     * Classes constructors.
     */
    public reScale()
    {
        Width = 0;
        Height = 0;
    }
    
    public reScale(int inputWidth, int inputHeight)
    {
        Width = inputWidth;
        Height = inputHeight;
    }
    
}
